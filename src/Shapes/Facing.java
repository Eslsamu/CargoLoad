package Shapes;
/**
 * Rotation of parcels
 * @author Blazej
 */
public enum Facing {
	
	UpA,   //Length is Z Axis, Width is Y 
	UpB,   //Length is Z Axis, Width is X
	RightA, //Length is Y Axis, Width is Z
	RightB, //Length is Y Axis, Width is X
	FrontA, //Length is X Axis, Width is Z
	FrontB; //Length is X Axis, Width is Y
}
